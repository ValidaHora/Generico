  public static char valorAscii(int c)
  {
    if (c <= 9)
      return (char)(c + '0');
    return (char)(c + 'A' - 10);
  }

  public static String decimalToHex(byte[] valorDecimal)
  {
    StringBuffer valorHexadecimal = new StringBuffer();
    for (int i = 0; i < valorDecimal.length; i++)
    {
      int c1 = (valorDecimal[i] & 0xF0) >> 4;
      int c2 = (valorDecimal[i] & 0x0F);
      valorHexadecimal.append(valorAscii(c1));
      valorHexadecimal.append(valorAscii(c2));
    }
    
    return valorHexadecimal.toString();
  }

